void main()
{
  var Day=7;
  switch(Day) {
    case 1:
      print('Its Monday,Weekday');
      break;
    case 2:
      print('Its Tuesday,Weekday');
      break;
    case 3:
      print('Its Wednesday,Weekday');
      break;
    case 4:
      print('Its Thursday,Weekday');
      break;
    case 5:
      print('Its Friday,Weekday');
      break;
    case 6:
      print('Its Saturday,Weekend');
      break;
    case 7:
      print('Its Sunday,Weekend');
      break;
    default:
      print('Invalid Day Entered');


  }


}